package client.tabelasDAO;

public class RecomendaPlantio {
	
	private  Integer esterco;
	private double superfosfato;
	private double calcario;
	private Integer fte;
	private double calagem;
	private String cobertura;
	
	public Integer getEsterco() {
		return esterco;
	}
	public void setEsterco(Integer esterco) {
		this.esterco = esterco;
	}
	public double getSuperfosfato() {
		return superfosfato;
	}
	public void setSuperfosfato(double superfosfato) {
		this.superfosfato = superfosfato;
	}
	public double getCalcario() {
		return calcario;
	}
	public void setCalcario(double calcario) {
		this.calcario = calcario;
	}
	public Integer getFte() {
		return fte;
	}
	public void setFte(Integer fte) {
		this.fte = fte;
	}
	public double getCalagem() {
		return calagem;
	}
	public void setCalagem(double calagem) {
		this.calagem = calagem;
	}
	public String getCobertura() {
		return cobertura;
	}
	public void setCobertura(String cobertura) {
		this.cobertura = cobertura;
	}
	

}
